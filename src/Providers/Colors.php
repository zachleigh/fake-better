    /**
     * Provider description.
     *
     * @var string
     */
    protected $description = 'Generate colors in various formats.';

    /**
     * Return a random HTML color name.
     *
     * @return string
     */
    public function colorName()
    {
        return static::NAMES[array_rand(static::NAMES)];
    }

    /**
     * Return a valid color hex.
     *
     * @return string
     */
    public function colorHex()
    {
        return sprintf('#%06X', mt_rand(0, 0xFFFFFF));
    }

    /**
     * Return a valid RGB color value.
     *
     * @return string
     */
    public function colorRgb()
    {
        return sprintf('rgb(%s)', $this->generateRgb());
    }

    /**
     * Return a valid RGBA color value.
     *
     * @return string
     */
    public function colorRgba()
    {
        return sprintf('rgba(%s, %.1f)', $this->generateRgb(), $this->generateFloat());        
    }

    /**
     * Generate the RGB values.
     *
     * @return string
     */
    protected function generateRgb()
    {
        return sprintf('%d, %d, %d', rand(1, 255), rand(1, 255), rand(1, 255));
    }

    /**
     * Return a random float between 0.1 and 1.0.
     *
     * @return float
     */
    protected function generateFloat()
    {
        return mt_rand(1, 10) / 10;
    }
